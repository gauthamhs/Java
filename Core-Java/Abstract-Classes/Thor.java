//Thor child class extending Odin Parent class.

public class Thor extends Odin {

	@Override
	public void strength() {
		System.out.println("Thor's hammer");
	}

	@Override
	public void attire() {
		System.out.println("Red");
	}

	@Override
	public void trait() {
		System.out.println("Arrogant and Powerful");
	}
}
